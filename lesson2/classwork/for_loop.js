// for (start; condition; change)
for (let i = 0; i < 5; i++) {  // loop runs 5 times
    console.log("Hello world!");
}

for (let i = 0; i < 3; i++) {  // loop runs 3 times
    console.log("Moo!");
}

let num = 6;
for (let i = 0; i < num; i++) {  // loop runs 6 times
    console.log("Bark!");
}

for (let i = 0; i < 5; i++) {  // i goes from 0 - 4
    console.log(i);
}

for (let i = 0; i < 5; i++) {
    console.log(i * 2);  // prints 0, 2, 4, 6, 8 (each i multiplied by 2)
}
